int	ft_isalpha(char *s1)
{
	int	i;

	i = 0;
	while (s1[i])
	{
		if (!(s1[i] >= 'a' && s1[1] <= 'z') && !(s1[i] >= 'A' && s1[i] <= 'Z'))
			return (0);
		i++;
	}
	return (1);
}
