int ft_isalnum(char *s1)
{
    int i;

    i = 0;
    while (s1[i])
    {
        if (!(s1[i] >= 'a' && s1[i] <= 'z') || !(s1[i] >= 'A' && s1[i] <= 'Z'))
        {
            if (!(s1[i] >= '0' && s1[i] <= '9'))
                return (0);
        }
        i++;
    }
    return (1);
}
